#==========================================================================================#



#------------------------------------------------------------------------------------------#
#    Join two lists.                                                                       #
#------------------------------------------------------------------------------------------#
list.join <<- function(x,y){
   ans        = unlist(c(x,y))
   names(ans) = NULL
   return     = ans
}#end ljoin
