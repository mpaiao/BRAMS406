#==========================================================================================#
#==========================================================================================#
#     This function is the same as standard split, except that it preserves the matrix     #
# shape in case x is a matrix.                                                             #
#------------------------------------------------------------------------------------------#
msplit <<- function(x,f,...){
   if (is.matrix(x)){
      x   = as.data.frame(x)
      ans = split(x=x,f=f,...)
      ans = lapply(X=ans,FUN=as.matrix)
   }else{
      ans = split(x=x,f=f,...)
   }#end if (is.matrix(x))
   return(ans)
}#end msplit
